def get_ioctl_definition(name: str) -> Optional[int]:
    return IOCTL_DEFINITIONS.get(name, None)


def ctl_code_from_ioctl(ioctl: int) -> str:
    device_type = (ioctl >> 16) & 0xFFFF
    function = (ioctl >> 2) & 0xFFF
    method = ioctl & 0x3
    access = (ioctl >> 14) & 0x3

    method_dict = {
        0: 'METHOD_BUFFERED',
        1: 'METHOD_IN_DIRECT',
        2: 'METHOD_OUT_DIRECT',
        3: 'METHOD_NEITHER',
    }
    access_dict = {
        0: 'FILE_ANY_ACCESS',
        1: 'FILE_READ_ACCESS',
        2: 'FILE_WRITE_ACCESS',
        3: 'FILE_READ_ACCESS | FILE_WRITE_ACCESS',
    }

    method_str = method_dict[method]
    access_str = access_dict[access]

    return f'CTL_CODE(0x{device_type:04x}, 0x{function:03x}, {method_str}, {access_str})'
